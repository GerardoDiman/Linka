import { initDatabase } from '../_lib/database.js';

export default async function handler(req, res) {
  const origin = req.headers.origin || '*';
  res.setHeader('Access-Control-Allow-Origin', origin);
  res.setHeader('Vary', 'Origin');
  res.setHeader('Access-Control-Allow-Methods', 'GET, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type, Authorization');
  res.setHeader('Access-Control-Allow-Credentials', 'true');

  if (req.method === 'OPTIONS') return res.status(200).end();
  if (req.method !== 'GET') return res.status(405).json({ error: 'MÃ©todo no permitido' });

  await initDatabase();

  // Stub simple de stats (en server unificado se calcula desde Notion)
  return res.status(200).json({ success: true, stats: { lastSync: new Date().toISOString() } });
}


